$cat_ids = [];
foreach ($categories as $name => $slug) {
    $term = term_exists($slug, 'category');
    if (!$term) {
        $term = wp_insert_term($name, 'category', ['slug' => $slug]);
    }
    $cat_ids[$name] = is_wp_error($term) ? 0 : (int) $term['term_id'];
}

/* ── 2. Helper: sideload a placeholder image as featured image ── */
function st_seed_set_thumbnail(int $post_id, int $seed): void
{
    if (has_post_thumbnail($post_id)) {
        return;
    }
    require_once ABSPATH . 'wp-admin/includes/media.php';
    require_once ABSPATH . 'wp-admin/includes/file.php';
    require_once ABSPATH . 'wp-admin/includes/image.php';

    $url = "https://picsum.photos/seed/spinestech-article-{$seed}/1200/675";
    $tmp = download_url($url);
    if (is_wp_error($tmp)) {
        return;
    }
    $file_array = [
        'name'     => "article-{$seed}.jpg",
        'tmp_name' => $tmp,
    ];
    $attach_id = media_handle_sideload($file_array, $post_id);
    if (is_wp_error($attach_id)) {
        @unlink($tmp);
        return;
    }
    set_post_thumbnail($post_id, $attach_id);
}

