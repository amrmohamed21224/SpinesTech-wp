/* ── 4. Insert posts ── */
$i = 0;
foreach ($articles as $article) {
    $i++;

    $existing = get_page_by_title($article['title'], OBJECT, 'post');
    if ($existing) {
        continue; // avoid duplicates if the script runs twice
    }

    $post_id = wp_insert_post([
        'post_title'    => $article['title'],
        'post_excerpt'  => $article['excerpt'],
        'post_content'  => $article['content'],
        'post_status'   => 'publish',
        'post_type'     => 'post',
        'post_author'   => 1,
        'post_date'     => date('Y-m-d H:i:s', strtotime("-{$i} days")),
        'post_category' => [$cat_ids[$article['category']] ?? 1],
    ]);

    if (!is_wp_error($post_id) && $post_id) {
        st_seed_set_thumbnail($post_id, $i);
    }
}

